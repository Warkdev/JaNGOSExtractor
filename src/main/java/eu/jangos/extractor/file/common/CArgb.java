package eu.jangos.extractor.file.common;

/**
 *
 * @author Warkdev
 */
public class CArgb {
    private byte r;
    private byte g;
    private byte b;
    private byte a;

    public byte getR() {
        return r;
    }

    public void setR(byte r) {
        this.r = r;
    }

    public byte getG() {
        return g;
    }

    public void setG(byte g) {
        this.g = g;
    }

    public byte getB() {
        return b;
    }

    public void setB(byte b) {
        this.b = b;
    }

    public byte getA() {
        return a;
    }

    public void setA(byte a) {
        this.a = a;
    }
    
    
}
